                <h2>Safety and security</h2>
                <span>Petty crime, including bag snatching, pick-pocketing, passport theft and theft from cars, is common, especially in larger cities, in and around major tourist attractions, on public transport, at major airports, railway stations and bus terminals.</span>
            </a>

