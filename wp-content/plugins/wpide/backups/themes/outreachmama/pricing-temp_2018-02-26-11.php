    <p>Thieves often work in groups to distract victims and rob them while their attention is diverted. </p>
    <p>Theft is particularly common on trains in Italy, including to and from Fiumicino Airport, near Rome. Thieves use a number of approaches to distract their victims, including: pretending to ask for directions while the train is stopped at a station; dropping attractive items on the floor of the train; blocking the view of luggage stored in overhead luggage racks; and throwing rubbish or ketchup at the victim. Often a member of the group will pretend to come to the assistance of the victim while others make off with the victim's valuables. For more information on train security and crime, see the 
        <a href="http://www.poliziadistato.it/articolo/view/22329/"> 
        <span>Polizia Di Stato</span></a> website (in English). 
    </p>
    <p>Passengers on night trains have been robbed while sleeping. Be extremely vigilant with your belongings at all times. </p>
    <p>Credit card and ATM fraud involving 'skimming' machines, which can store card data, occurs. Monitor transaction statements and only use ATMs in secure locations such as banks, shops or malls. Police have warned that counterfeit European currency is in circulation. When making purchases, carefully examine any notes you receive in change. </p>
    <p>A number of tourists have been robbed and assaulted after accepting 'spiked' food or drink. In Rome, many of these attacks have taken place around Termini station, tourist areas such as the Colosseum, and in bars and cafes near Campo dei Fiori and Piazza Navona. In Florence and Naples, attacks have occurred in the vicinity of train stations, and in bars and cafes in the city centres. Some victims have been sexually assaulted or have required hospitalisation.</p>
    <p>Robberies from cars at traffic lights, rest stops and service stations occur in Italy. There are reports of thieves slashing tires or staging roadside emergencies to persuade drivers to pull over and get out of their cars. While the driver is distracted, the thieves steal personal belongings. </p>
    <p>Car break-in and theft is also common. Popular targets for thieves are unattended campervans or mobile homes, whether parked at camping sites or in the streets in the vicinity of historic sites. Never leave valuables in your car. When you leave your car, ensure it's locked, even if you will only be away for a short period of time. Many Australians have had their belongings, including passports and other valuables, stolen from unattended vehicles. </p>
    <h3>Civil unrest/Political tension </h3>
    <p>Demonstrations and strikes are a common occurrence in Italy. They can cause building closures, particularly in tourist areas, and disruptions to public transport services, including air, shipping, train, bus, tram and taxi services, roadblocks and petrol station closures, leading to delays and cancellations. The Italian 
        <a href="http://www.mit.gov.it/mit/site.php?p=scioperi"> 
