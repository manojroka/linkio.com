    <p>When catching public transport you must validate your ticket prior to boarding a train or a metro, and immediately upon boarding a bus or tram. Failure to validate your ticket can result in on-the-spot fines. In most cities, you're required to pre-purchase bus and train tickets as there are no purchasing facilities once you board a bus or train. Pre-paid tickets are usually available from tobacconists or bars (coffee shops) that display the public transport company's logo/name. Automatic ticket machines are located at every metro and major train station. </p>
    <h3>Driving </h3>
    <p>In Italy, holders of a valid Australian driver's licence, who aren't residents of Italy, may drive for an unlimited period of time provided they have an International Drivers Permit (IDP) or an official translation of the Australian licence. The translation must be prepared by a 'traduttore giurato' (official translator) in Italy. A list of official translators may be found in the 
        <a href="http://www.paginegialle.it/">Italian Yellow Pages</a>. When hiring a car in Italy, you may be required to provide additional ID to your international driver's licence, such as a passport. 
    </p>
    <p>The holder of an Australian licence who takes up legal residence (residenza) can drive on an Australian driver's licence accompanied by the IDP or translation for up to one year only. If you take up residency and remain in Italy beyond one year, you need to apply for an Italian driver's licence. Italy doesn't allow the conversion of an Australian licence, so you will be required to undertake a written test and driving exam conducted in Italian (the exam can be conducted in German or French, under certain circumstances). Australians should contact the 
        <a href="http://protocol.dfat.gov.au/Mission/view.rails?id=97"> 
        <span>Italian Embassy</span></a> in Australia for further information on Italian driver's licence requirements. 
    </p>
    <p>Driving in Italy can be dangerous and driving conditions can seem chaotic compared to Australia. On-the-spot fines are payable for a range of minor traffic offences. It's mandatory to use headlights on main roads outside the urban areas and highways, including during the day. More information:
        <a href="/guide/all-travellers/getting-around/pages/road-safety-and-driving.aspx"> 
        <span>Road safety and driving. </span></a> 
    </p>
    <p>Vehicle access to the centres of many Italian cities are restricted to help reduce congestion. Traffic Restricted Zones (ZTL), and their hours of operation, vary from city to city. Fines are levied on vehicles that don't carry ZTL passes. It's unlikely that hire cars will have a ZTL pass. </p>
    <p>If staying in commercial accommodation in the centre of an Italian city, ask your hotel about traffic restrictions in the area prior to your arrival. For non-residents, the law allows authorities up to five years to identify the person responsible for the infringement and 360 days after the identification to issue the fine. </p>
    <p>Many municipalities have outsourced the collection of traffic fines. European Municipal Outsourcing (EMO) handles the majority of the fines, but fines may be issued by other agencies. Further information on ZTL restrictions can be found on the websites of the individual municipalities (comune). </p>
    <h3>Taxis and travel by foot</h3>
    <p>Always travel in licensed taxis, which can be identified by appropriate signage, roof lights and meters. Unauthorised taxis don't carry meters and charge disproportionately large fares when you reach your destination. </p>
    <p>Pedestrian fatalities are common throughout Italy. Exercise particular care when crossing roads, including at controlled pedestrian crossings. Although required by law, motorists in Italy will often not give way to pedestrians.</p>
